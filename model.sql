CREATE TABLE tbj_usuario (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome VARCHAR2(100) NOT NULL,
    email VARCHAR2(100) UNIQUE NOT NULL,
    senha VARCHAR2(100) NOT NULL,
    cpf VARCHAR2(14) UNIQUE NOT NULL,
    dt_nascimento DATE NOT NULL,
    estado VARCHAR2(50) NOT NULL,
    numero_cnh VARCHAR2(20) NOT NULL
);

CREATE TABLE tbj_veiculo (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    modelo VARCHAR2(100) NOT NULL,
    cor VARCHAR2(20) NOT NULL,
    placa VARCHAR2(10) UNIQUE NOT NULL,
    ponto VARCHAR2(100) NOT NULL
);

CREATE TABLE tbj_ponto_aluguel (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome VARCHAR2(100) NOT NULL,
    endereco VARCHAR2(200) NOT NULL
);

CREATE TABLE tbj_agendamento (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_usuario NUMBER NOT NULL REFERENCES tbj_usuario(id),
    id_veiculo NUMBER NOT NULL REFERENCES tbj_veiculo(id),
    data_agendamento TIMESTAMP DEFAULT SYSTIMESTAMP
);
